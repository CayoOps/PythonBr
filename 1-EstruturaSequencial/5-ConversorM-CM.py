#5. Faça um Programa que converta metros para centímetros.

print("================================================================")
print("Bem-vindo ao conversor de Metros para Centímetros!!")
print("Se der erro não é um número!!")
print("================================================================")
metros = float(input("Digite o valor de Metros: "))

centimetros = metros * 100

print(metros, "Metros corresponde á, %.1f Centímetros!" %centimetros)

print("================================================================")
